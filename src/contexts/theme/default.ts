// merge two objects or join two arrays together
const deepMerge = <T = object | any[]>(src: T, target: T): T => {
  if (Array.isArray(src) && Array.isArray(target)) {
    return [...src, ...target] as T;
  }

  if (typeof src === "object" && typeof target === "object") {
    return {
      ...src,
      ...Object.keys(target as object).reduce(
        (acc, key) => ({
          ...acc,
          [key]:
            key in (src as object)
              ? deepMerge((src as any)[key], (target as any)[key])
              : (target as any)[key],
        }),
        {},
      ),
    };
  }

  return src;
};

type DeepPartial<T> = T extends object
  ? {
      [P in keyof T]?: DeepPartial<T[P]>;
    }
  : T;

export const applyTheme = (theme: DeepPartial<Theme>): Theme => {
  return deepMerge(theme as Theme, defaultTheme);
};
